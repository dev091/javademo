package Array;

public class Jaggedarray {
    public static void main(String[] args) {
        int a[][] = {{5,6,2}, // row 0
                     {7,9,8,4},   // 8 = row 1 column 2
                     {3,4,2,1,5},        // row 2
                     {5,4,7,0}}; // row 3
        System.out.println(a[3][1]); // row - column
    }
}

    