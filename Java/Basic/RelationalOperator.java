package Basic;

public class RelationalOperator {

    public static void main(String[] args) {
   	 
   	int p = 10 , q = 33  ;
  	 
   	System.out.println(p==q);
   	System.out.println(p!=q);
   	System.out.println(p>q);
   	System.out.println(p<q);
   	System.out.println(p>=q);
   	System.out.println(p<=q);
    }

}
